let arr = [1, 2, 3];
console.log(arr);

//add in END
arr.push(4);
console.log(arr)

//remove in END
arr.pop();
console.log(arr)

//add multiple element
arr.push(5, 6);
console.log(arr)

//add in beginning
arr.unshift(0);
console.log(arr);

//Remove from begginnig
arr.shift();
console.log(arr);

arr.fill(4 , 1, 3, 5);
console.log(arr);