//Assignment Operators
//- =
// - to assign the right-hand side value to the left-hand side variable
// - let x = 10; // assigns the value 10 to variable x
// -x = 20; // assigns the value 20 to variable x, overwriting the previous value

let x = 30; // assigns 30 to x
x = 40; // assigns 40 to x, overwriting the previous value of 30
x = 50; // assigns 50 to x, overwriting the previous value of 40
x = 90; // assigns 90 to x, overwriting the previous value of 50
console.log("Final value of x:", x); // 90