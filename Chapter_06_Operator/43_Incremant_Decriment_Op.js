//Incremanent and Decrement Operator
//1. Pre Increment: ++x (increments x by 1 before using it)
//2. Post Increment: x++ (uses x first, then increments it by 1)
//3. Pre Decrement: --x (decrements x by 1 before using it)
//4. Post Decrement: x-- (uses x first, then decrements it by 1)

let a = 10;
let b = ++a; // Pre Increment: a is incremented to 11, then assigned to b
//Pre Increment  -> ++a -> . increase the value then assign it
console.log(b); // 11
console.log(a); // 11





