let x = 10;
x += 10;   // compound assignment operator for addition (x = x + 10)
console.log("Value of x after addition assignment:", x); // 20


 x -= 3;
console.log("Value of x after subtraction assignment:", x); // 17

x *= 2;
console.log("Value of x after multiplication assignment:", x); // 34

x /= 4;
console.log("Value of x after division assignment:", x); // 17

x %= 5;
console.log("Value of x after modulus assignment:", x); // 2  