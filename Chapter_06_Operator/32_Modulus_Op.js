//Modulus Operator
let result = 13 % 7; // returns the remainder of the division of 13 by 7
console.log("Result of 13 % 7:", result); // 6

console.log("Result of 20 % 3:", 20 % 3); // 2
console.log("Result of 15 % 4:", 15 % 4); // 3
console.log("Result of 10 % 2:", 10 % 2);


//n % 2 == 1 --> odd number
//n % 2 == 0 --> even number